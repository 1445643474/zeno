struct RadiancePRD
{
    // TODO: move some state directly into payload registers?
    float3       emitted;
    float3       radiance;
    float3       attenuation;
    float3       origin;
    float3       direction;
    float        opacity;
    unsigned int seed;
    unsigned int flags = 0;
    int          countEmitted;
    int          done;
    int          pad;
};


